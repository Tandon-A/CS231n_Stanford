def svm_loss(x, y):
    """
    Computes the loss and gradient using for multiclass SVM classification.

    Inputs:
    - x: Input data, of shape (N, C) where x[i, j] is the score for the jth
      class for the ith input.
    - y: Vector of labels, of shape (N,) where y[i] is the label for x[i] and
      0 <= y[i] < C

    Returns a tuple of:
    - loss: Scalar giving the loss
    - dx: Gradient of the loss with respect to x
    """
    loss, dx = None, None
    ###########################################################################
    # TODO: Implement loss and gradient for multiclass SVM classification.    #
    # This will be similar to the svm loss vectorized implementation in       #
    # cs231n/classifiers/linear_svm.py.                                       #
    ###########################################################################
    # *****START OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****

    y_one_hot = np.zeros_like(x)
    y_one_hot[np.arange(x.shape[0]), y] = 1
    x_correct = np.expand_dims(x[np.arange(x.shape[0]), y], axis = 1)
    loss = np.maximum(0, x - x_correct + 1)
    bool_max = np.where(loss > 0, 1, 0)
    loss = loss * (1 - y_one_hot)
    loss = np.mean(np.sum(loss, axis = 1))

    dloss = 1/x.shape[0] * (1 - y_one_hot)
    dbool_max = bool_max * dloss
    dx = 1 * dbool_max
    dx_filter = -1 * np.sum(dbool_max, axis = 1)
    dx[np.arange(x.shape[0]), y] += dx_filter


    # *****END OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****
    ###########################################################################
    #                             END OF YOUR CODE                            #
    ###########################################################################
    return loss, dx


def softmax_loss(x, y):
    """
    Computes the loss and gradient for softmax classification.

    Inputs:
    - x: Input data, of shape (N, C) where x[i, j] is the score for the jth
      class for the ith input.
    - y: Vector of labels, of shape (N,) where y[i] is the label for x[i] and
      0 <= y[i] < C

    Returns a tuple of:
    - loss: Scalar giving the loss
    - dx: Gradient of the loss with respect to x
    """
    loss, dx = None, None
    ###########################################################################
    # TODO: Implement the loss and gradient for softmax classification. This  #
    # will be similar to the softmax loss vectorized implementation in        #
    # cs231n/classifiers/softmax.py.                                          #
    ###########################################################################
    # *****START OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****

    num_train = x.shape[0]

    x_max = np.max(x, axis = 1, keepdims = True)
    x_stable = x - x_max
    x_softmax = np.exp(x_stable)/np.sum(np.exp(x_stable), axis = 1, keepdims = True)
    loss = -np.log(x_softmax[np.arange(num_train), y])
    loss = np.sum(loss)/num_train

    dx = x_softmax
    dx[np.arange(num_train), y] -= 1
    dx /= num_train

    # *****END OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****
    ###########################################################################
    #                             END OF YOUR CODE                            #
    ###########################################################################
    return loss, dx
